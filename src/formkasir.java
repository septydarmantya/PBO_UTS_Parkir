    private void selectData() {
        String kolom[] = {"noresi","nopolisi","jeniskendaraan","namapetugas","tanggalmasuk","tanggalkeluar","jumlahhari","totalbayar"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_transaksi";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()) {
                String noresi = rs.getString(1);
                String nopolisi = rs.getString(2);
                String jeniskendaraan = "";
                if ("Motor".equals(rs.getString(3))){
                    jeniskendaraan = "Motor";
                } else {
                    jeniskendaraan = "Mobil";
                }
                String namapetugas = rs.getString(4);
                String tanggalmasuk = rs.getString(5);
                String tanggalkeluar = rs.getString(6);
                String jumlahhari = rs.getString(7);
                String totalbayar = rs.getString(8);
                String data[] = {noresi,nopolisi,jeniskendaraan,namapetugas,tanggalmasuk,tanggalkeluar,jumlahhari,totalbayar};
                dtm.addRow(data);
            }
        } catch (SQLException ex){
            Logger.getLogger(formkasir.class.getName()).log(Level.SEVERE, null, ex);
        }
        data.setModel(dtm);
    }

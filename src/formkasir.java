    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String masuk = dateFormat.format(tanggalmasuk.getDate());
        String keluar = dateFormat.format(tanggalkeluar.getDate());
        String JK = "";
       
        String nor = noresi.getText();
        String nop = nopol.getText();
        String np = namapetugas.getText();
        String jh = jumlahhari.getText();
        String tb = totalbayar.getText();
        
        if (motor.isSelected()) {
            JK = "Motor";
        } else {
            JK = "Mobil";
        }
        if (noresi.getText().equals("") || 
           nopol.getText().equals("") ||
           JK.equals("") ||
           namapetugas.getText().equals("") ||
           masuk.equals("") ||
           keluar.equals("")||
           jumlahhari.getText().equals("") ||
           totalbayar.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "Harap Lengkai Data", "Error",
                    JOptionPane.WARNING_MESSAGE);
        }else{            
            String SQL ="INSERT INTO tb_transaksi (noresi,nopolisi,jeniskendaraan,namapetugas,tanggalmasuk,tanggalkeluar,jumlahhari,totalbayar)" 
                    + "VALUES('"
                    +noresi.getText()+"','"
                    +nopol.getText()+"','"
                    +JK+"',"
                    + "'"
                    +namapetugas.getText()+"','"
                    +masuk+"','"
                    +keluar+"','"
                    +jumlahhari.getText()+"','"
                    +totalbayar.getText()+")";
                    
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                 JOptionPane.showMessageDialog(this, "Data berhasil diupdate","Sukses", JOptionPane.INFORMATION_MESSAGE);
                 selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Data gagal diupdate", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_saveActionPerformed

